    public final void tableListCities(ArrayList<Citys> cities) {
        tableModel.setRowCount(0);
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        renderer.setHorizontalAlignment(DefaultTableCellRenderer.LEFT);
        tblCities.getColumnModel().getColumn(0).setCellRenderer(renderer);
        tblCities.getColumnModel().getColumn(1).setCellRenderer(renderer);
        tblCities.getColumnModel().getColumn(2).setCellRenderer(renderer);
        for (Citys city : cities) {
            Object[] row = {city.getCity_id(), city.getCity_name(),
                city.getCountry_id()};
            tableModel.addRow(row);
        }
    }

    public final void timer(int time) {
        this.timer = new Timer(time * 1000, actionEvent -> warningLabel.setText(""));
        timer.setRepeats(false);
        timer.start();
    }

