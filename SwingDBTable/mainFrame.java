    private void filterButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterButton2ActionPerformed
        ArrayList<Citys> cities;
        if (cityIDText1.getText().isEmpty()) {
            restartList();
            return;
        }
        cities = cityManager.citiesFilterCountryID(cityIDText1.getText());
        try {
            tableListCities(cities);
        } catch (NullPointerException ex) {
            warningLabel.setText("ID not found...");
            timer(4);
            restartList();
        }
    }//GEN-LAST:event_filterButton2ActionPerformed

